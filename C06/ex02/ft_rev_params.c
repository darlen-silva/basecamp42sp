#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;

	if (ac > 0)
	{
		while (--ac > 0)
		{
			j = 0;
			while (av[ac][j])
			{
				write(1, &av[ac][j], 1);
				j++;
			}
			write(1, "\n", 1);
		}
	}
	return (0);
}
