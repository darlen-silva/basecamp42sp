int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	while (i * i <= nb + 10)
	{
		// if (i > 5)
		// 	while (i % 2 == 0 || i % 3 == 0 || i % 5 == 0)
		// 			i++;
		if (nb % i == 0 && i * i <= nb)
			return (0);
		i++;
	}
	return (1);
}
