char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	count;

	count = 0;
	while (src[count] != '\0' && count < n)
	{
		dest[count] = src[count];
		count++;
	}
	while (count < n)
	{
		dest[count] = '\0';
		count++;
	}
	return (dest);
}

#include <stdio.h>

int main(void)
{
	char a[10] = "ola mundo";
	char b[] = "hello";

	printf("%s\n", ft_strncpy(a, b, 10));
}